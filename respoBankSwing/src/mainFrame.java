    private void paraCek(double cekilecek) {
        try {
            getInfo(user);
            double total = user.getMoney()- cekilecek;
            int response = JOptionPane.showConfirmDialog(this, "Para çekme işlemini onaylıyor musun?\nÇekilecek para : "+cekilecek+" \nİşlem sonucu paranız : " + total, "Para yatırma", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            txtParaCekmeMiktari.setText("");
            if(total < -500) {
                double cekilebilinir = user.getMoney()-(-500);
                lblTransactionMsg.setText("<html>Çekmek istediğiniz miktar ile hesap bakiyeniz -500'e ulaşmakta. <br>Güncel hesap bakiyeniz : " + user.getMoney() + "<br>Çekilebilinir hesap bakiyesi : " + cekilebilinir + "</html>");
                Parent.removeAll();
                Parent.add(TransactionComplete);
                Parent.repaint();
                Parent.revalidate();
            } else {
                if(response == JOptionPane.YES_OPTION) {
                    withdrawMoney withdraw = new withdrawMoney();
                    withdraw.money(user, cekilecek);
                    lblTransactionMsg.setText("<html>Hesabınızdan "+cekilecek+"₺ para çektiniz.<br>Güncel hesap bakiyeniz : "+total+" </html>");
                    Parent.removeAll();
                    Parent.add(TransactionComplete);
                    Parent.repaint();
                    Parent.revalidate();
                } else {
                    lblWithdrawMoneyErrMsg.setText("İşlem iptal edildi.");
                }
            }
        } catch (SQLException ex) {
            lblWithdrawMoneyErrMsg.setText("Bağlantı hatası oluştu.");
        }
    
    }
    
