    private void paraYatir(double yatirilacak) {
        double total = user.getMoney() + yatirilacak;
        int response = JOptionPane.showConfirmDialog(this, "Para yatırma işlemini onaylıyor musun?\nYatırılacak para : "+yatirilacak+" \nToplam olacak para : " + total, "Para yatırma", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        txtParaMiktari.setText("");
        if(response == JOptionPane.YES_OPTION) {
            try {
                depositMoney deposit = new depositMoney();
                deposit.money(user, yatirilacak);
                getInfo(user);
                lblTransactionMsg.setText(yatirilacak+"₺'a  yatırıldı.\nToplam paranız : " + user.getMoney());
                Parent.removeAll();
                Parent.add(TransactionComplete);
                Parent.repaint();
                Parent.revalidate();
            } catch (SQLException e) {
                lblDepositMoneyErrMsg.setText("Bağlantı oluşurken sorun oluştu.");
            }
        } else {
            lblDepositMoneyErrMsg.setText("İşlem iptal edildi.");
        }
    }
    
