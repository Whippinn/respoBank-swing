    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if(!txtLoginEmail.getText().isEmpty() && (passLoginPassword.getPassword().length != 0)) {
            try {
                user.setEmail(txtLoginEmail.getText());
                user.setPassword(String.valueOf(passLoginPassword.getPassword()));
                login login = new login();
                if(login.control(user)) {
                    Parent.removeAll();
                    Parent.add(menuPanel);
                    Parent.repaint();
                    Parent.revalidate();
                    getInfo(user);
                    lblMerhabaName.setText("Merhaba, " + user.getFirstName() + " " + user.getLastName());
                    lblMoney.setText("Bakiye : " + user.getMoney());
                } else {
                lblErrorMessage.setText("Giriş yapılırken sorun oluştu.");}
            } catch (SQLException ex) {
                lblErrorMessage.setText("Bağlantı hatası oluştu.");
            }
        } else {
            lblErrorMessage.setText("Tüm alanları doldurunuz.");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

